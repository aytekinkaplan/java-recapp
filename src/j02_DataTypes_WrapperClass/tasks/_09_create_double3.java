package j02_DataTypes_WrapperClass.tasks;

public class _09_create_double3 {

    public static void main(String[] args) {

        /* Create a double with the value 10.01.
           Print the double. */

        // Example 1: Double with value 10.01
        double number = 10.01;
        System.out.println("Double value: " + number);

        // Example 2: Another double value
        double anotherNumber = 20.99;
        System.out.println("Another double value: " + anotherNumber);

        // Example 3: Adding two doubles
        double sum = number + anotherNumber;
        System.out.println("Sum of two doubles: " + sum);

        // Example 4: Subtracting two doubles
        double difference = anotherNumber - number;
        System.out.println("Difference between doubles: " + difference);

        // Example 5: Dividing two doubles
        double divisionResult = anotherNumber / number;
        System.out.println("Division of anotherNumber by number: " + divisionResult);
    }
}
