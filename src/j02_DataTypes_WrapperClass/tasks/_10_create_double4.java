package j02_DataTypes_WrapperClass.tasks;

public class _10_create_double4 {

    public static void main(String[] args) {

        /* Create a double with the value 550.24.
           Print the double. */

        // Example 1: Double with value 550.24
        double number = 550.24;
        System.out.println("The number is: " + number);

        // Example 2: Another double with a different value
        double anotherNumber = 300.75;
        System.out.println("Another number: " + anotherNumber);

        // Example 3: Sum of two doubles
        double sum = number + anotherNumber;
        System.out.println("Sum of the two numbers: " + sum);

        // Example 4: Difference between two doubles
        double difference = number - anotherNumber;
        System.out.println("Difference between the two numbers: " + difference);

        // Example 5: Multiplication of two doubles
        double product = number * 2;
        System.out.println("Product of the number and 2: " + product);

        // Example 6: Division of two doubles
        double division = number / anotherNumber;
        System.out.println("Division of number by anotherNumber: " + division);

    }
}
