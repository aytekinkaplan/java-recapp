package j02_DataTypes_WrapperClass.tasks;

public class _07_create_double1 {

    public static void main(String[] args) {

        /* Create a double with value 120.
           Print the double. */

        // Example 1: Double with value 120
        double number = 120.0;
        System.out.println("Double value 1: " + number);

        // Example 2: Double with a decimal value
        double decimalNumber = 123.45;
        System.out.println("Double value 2: " + decimalNumber);

        // Example 3: Double resulting from a division
        double divisionResult = 100.0 / 3.0;
        System.out.println("Division result: " + divisionResult);

        // Example 4: Performing mathematical operations with doubles
        double sum = number + decimalNumber;
        System.out.println("Sum of two doubles: " + sum);

        // Example 5: Multiplying doubles
        double product = number * decimalNumber;
        System.out.println("Product of two doubles: " + product);

    }
}
