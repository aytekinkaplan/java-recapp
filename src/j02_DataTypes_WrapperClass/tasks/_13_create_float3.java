package j02_DataTypes_WrapperClass.tasks;

public class _13_create_float3 {

    public static void main(String[] args) {

        /* Create a float with the value 3.55f.
           Print the float. */

        // Example 1: Float with value 3.55f
        float f = 3.55f;
        System.out.println("Float value: " + f);

        // Example 2: Another float value
        float anotherFloat = 2.15f;
        System.out.println("Another float value: " + anotherFloat);

        // Example 3: Sum of two floats
        float sum = f + anotherFloat;
        System.out.println("Sum of the two floats: " + sum);

        // Example 4: Difference between two floats
        float difference = f - anotherFloat;
        System.out.println("Difference between the two floats: " + difference);

        // Example 5: Multiplication of floats
        float product = f * 2.5f;
        System.out.println("Product of the float and 2.5: " + product);

        // Example 6: Division of two floats
        float division = f / anotherFloat;
        System.out.println("Division of f by anotherFloat: " + division);

    }
}
