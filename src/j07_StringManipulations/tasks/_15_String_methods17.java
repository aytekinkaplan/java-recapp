package j07_StringManipulations.tasks;

public class _15_String_methods17 {

    public static void main(String[] args) {

        /*
         * Task:
         * - Create a string with the value "apple".
         * - Convert the string to uppercase and print the result.
         */

        System.out.println("Uppercase: " + "apple".toUpperCase()); // Convert to uppercase and print

        /*
         * Explanation:
         * - The toUpperCase() method converts all lowercase letters in the string to uppercase.
         * - This is commonly used for standardizing text formats or for displaying text in a consistent style.
         */
    }
}
