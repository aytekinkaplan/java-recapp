package j07_StringManipulations.tasks;

public class _07_String_methods04 {

    public static void main(String[] args) {

        /*
         * Task:
         * - Create a string with the value "OraNge".
         * - Convert the string to lowercase and print the result.
         * - Example: "APPLE" -> "apple"
         */

        String str = "OraNge"; // Define the string
        System.out.println("Lowercase: " + str.toLowerCase()); // Convert to lowercase and print

        /*
         * Explanation:
         * - The toLowerCase() method converts all uppercase letters in the string to lowercase.
         * - This is useful for standardizing text formats, such as making text case-insensitive for comparisons.
         */
    }
}
