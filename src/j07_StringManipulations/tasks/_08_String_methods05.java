package j07_StringManipulations.tasks;

public class _08_String_methods05 {

    public static void main(String[] args) {

        /*
         * Task:
         * - Create a string with the value "New Jersey".
         * - Convert the string to uppercase and print the result.
         */

        String str = "New Jersey"; // Define the string
        System.out.println("Uppercase: " + str.toUpperCase()); // Convert to uppercase and print

        /*
         * Explanation:
         * - The toUpperCase() method converts all lowercase letters in the string to uppercase.
         * - This is commonly used to ensure consistent formatting for titles, headings, or standardized text formats.
         */
    }
}
