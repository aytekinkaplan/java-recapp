package j07_StringManipulations.tasks;

public class _09_String_methods06 {

    public static void main(String[] args) {

        /*
         * Task:
         * - Create a string with the value "New York".
         * - Convert the string to lowercase and print the result.
         */

        String str = "New York"; // Define the string
        System.out.println("Lowercase: " + str.toLowerCase()); // Convert to lowercase and print

        /*
         * Explanation:
         * - The toLowerCase() method converts all uppercase letters in the string to lowercase.
         * - This method is often used for case-insensitive comparisons and to ensure consistent text formats.
         */
    }
}
