package j04_JavaOperators.tasks;

public class Task07 {

    public static void main(String[] args) {
        
        /*
         * Question:
         * Divide 120 by 60, multiply the result by 10, add 20 to it, 
         * and then subtract 40 from the result. Print the final result.
         */
        
        // Step 1: Divide 120 by 60
        int result = 120 / 60;
        
        // Step 2: Multiply the result by 10
        result = result * 10;
        
        // Step 3: Add 20 to the result
        result = result + 20;
        
        // Step 4: Subtract 40 from the result
        result = result - 40;
        
        // Print the final result
        System.out.println("The final result is: " + result);
    }
}
